public class Average2 {
    /*
    Given two numbers, num1 and num2, output the average.
    */
    public static void main(String[] args) {
        double numOne = 1.5;
        double numTwo = 3.5;

        double theTotal = numOne + numTwo;
        double theAverage = theTotal / 2;

        System.out.println("The average is: " + theAverage);
    }
}
